    public static byte[] convertStringToBytes(String input ){
        byte[] byteArray = new byte[input.length() << 1];
        for (int i = 0; i < input.length(); i++) {
            byteArray[i << 1] = (byte) (input.charAt(i) % 256);
            byteArray[(i << 1) + 1] = (byte) (input.charAt(i) / 256);
        }
        return byteArray;
    }
